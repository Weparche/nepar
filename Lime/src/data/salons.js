function withCoordinates(salon) {
  const coords = SALON_COORDINATES[salon.slug]
  const imageUrl = getSalonImageUrl(salon)
  return {
    ...salon,
    ...(coords ? { lat: coords.lat, lng: coords.lng } : {}),
    ...(imageUrl ? { imageUrl } : {}),
  }
}

export const salons = rawSalons.map(withCoordinates)

export function getSalonBySlug(slug) {
  return salons.find((s) => s.slug === slug)
}

export function getSalonsByCity(citySlug) {
  return salons.filter((s) => s.citySlug === citySlug)
}

export function getSalonsByCategory(categorySlug, citySlug = null) {
  return salons.filter(
    (s) => s.categorySlug === categorySlug && (!citySlug || s.citySlug === citySlug),
  )
}

export function getSalonCountByCity(citySlug) {
  return salons.filter((s) => s.citySlug === citySlug).length
}

export function getPopularSalons(limit = 6) {
  return [...salons].sort((a, b) => b.reviewCount - a.reviewCount).slice(0, limit)
}

export function getTodayAvailableInCity(citySlug, limit = 4) {
  return salons
    .filter((s) => s.citySlug === citySlug && s.availableToday)
    .slice(0, limit)
}
