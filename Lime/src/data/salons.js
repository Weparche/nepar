import { CATEGORY_SLUG_TO_FULL } from './categories'

const defaultServices = (items) =>
  items.map((s, i) => ({ id: `svc-${i}`, ...s }))

const defaultEmployees = (names) =>
  names.map((name, i) => ({ id: `emp-${i}`, name, role: 'Stručnjak' }))

const defaultReviews = (items) =>
  items.map((r, i) => ({ id: `rev-${i}`, ...r }))

function salon({
  id,
  slug,
  name,
  categorySlug,
  area,
  city,
  citySlug,
  rating,
  reviewCount,
  priceFrom,
  availableToday = true,
  onlinePayment = true,
  appointments = ['10:00', '14:00'],
  address,
  services,
  employees,
  about,
  reviews,
  schedule,
  imageIndex,
}) {
  return {
    id,
    slug,
    name,
    category: CATEGORY_SLUG_TO_FULL[categorySlug],
    categorySlug,
    area,
    city,
    citySlug,
    rating,
    reviewCount,
    priceFrom,
    availableToday,
    onlinePayment,
    appointments,
    address,
    services: defaultServices(services),
    employees: defaultEmployees(employees),
    about,
    reviews: defaultReviews(reviews),
    schedule,
    imageIndex,
  }
}

